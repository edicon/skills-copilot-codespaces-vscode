function skillsMember() {
    var member = document.getElementById("member");
    var skills = document.getElementById("skills");
    var projects = document.getElementById("projects");
    var contact = document.getElementById("contact");
    var memberInfo = document.getElementById("memberInfo");
    var skillsInfo = document.getElementById("skillsInfo");
    var projectsInfo = document.getElementById("projectsInfo");
    var contactInfo = document.getElementById("contactInfo");
    member.style.backgroundColor = "#f5f5f5";
    skills.style.backgroundColor = "#e9ecef";
    projects.style.backgroundColor = "#e9ecef";
    contact.style.backgroundColor = "#e9ecef";
    memberInfo.style.display = "block";
    skillsInfo.style.display = "none";
    projectsInfo.style.display = "none";
    contactInfo.style.display = "none";
}